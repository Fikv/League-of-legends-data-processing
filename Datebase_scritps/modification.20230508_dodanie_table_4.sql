CREATE TABLE MATCH_DATA(
    PLAYER_ID INTEGER,
    MATCH_ID TEXT,
    DATE_OF_MATCH TIMESTAMP,
    WIN BOOLEAN,
    RANKED_SOLO BOOLEAN,
    RANKED_DUO BOOLEAN,
    GAME_CREATION BIGINT,
    GAME_DURATION BIGINT,
    GAME_END_TIMESTAMP BIGINT,
    GAME_MODE TEXT,
    GAME_START_TIMESTAMP BIGINT,
    GAME_TYPE TEXT,
    MAP_ID INTEGER,
    EARLIEST_BARON DECIMAL,
    DETECTOR_WARDS_PLACED INTEGER, 
    FIRST_BLOOD_KILL BOOLEAN,
    ENEMY_MISSING_PING INTEGER,
    VISION_SCORE INTEGER,
    WARD_PLACED INTEGER, 
    ASSISTS_ME_PINGS INTEGER,
    BARON_KILLS INTEGER,
    DRAGON_KILLS INTEGER,
    HERALD_KILLS INTEGER,
    FIRST_TURRET BOOLEAN,
    CONTROL_WARDS_PLACED INTEGER,
    TEAM_ID TEXT,
    WIN BOOLEAN,
    FOREIGN KEY (PLAYER_ID) REFERENCES PLAYER (PLAYER_ID),
    FOREIGN KEY (MATCH_ID) REFERENCES MATCHES (MATCH_ID)
)